groceries = ['milk', 'cereal', 'steak', 'garlic bread', 'butter', 'ice cream']

print(groceries)

groceries.append('guacamole')
print(groceries)

groceries.remove('garlic bread')
print(groceries)

groceries.pop()
print(groceries)

groceries.insert(3, 'oranges')
print(groceries)

groceries.pop(3)
print(groceries)

groceries.sort()
print(groceries)

groceries.sort(reverse=True)
print(groceries)

print(groceries[-1])

print(groceries[-3])

print(len(groceries))

print(groceries[4])